  INSTPAT("0000000 ????? ????? 101 ????? 00110 11", srliw  , I, R(dest) = SBH(src1, 4, false) >> src2);
  INSTPAT("0100000 ????? ????? 101 ????? 00110 11", sraiw  , I, R(dest) = SI(SBH(src1, 4, true)) >> src2);
